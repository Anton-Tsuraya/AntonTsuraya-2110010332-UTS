    public void TampilAlamat(String cari){
    ArrayList<Buku>listdata = new ArrayList<Buku>();
        if(cari.equals("")){
            listdata = bkalamat;
        } else {
            for (Buku buku:bkalamat){
                if(buku.getNama().toLowerCase().contains(cari.toLowerCase())){
                    listdata.add(buku);
                }
            }
        }
        DefaultTableModel  dtmbk = (DefaultTableModel) tblBuku.getModel();
        Object[] row = new Object[5];
        for (int i = 0; i < listdata.size(); i++) {
            row[0] = listdata.get(i).getId();
            row[1] = listdata.get(i).getNama();
            row[2] = listdata.get(i).getTelepon();
            row[3] = listdata.get(i).getKelamin();
            row[4] = listdata.get(i).getAlamat();
            dtmbk.addRow(row);
        }
    }
    
    public final void RefreshData(String cari){
        DefaultTableModel  dtmbk = (DefaultTableModel) tblBuku.getModel();
        dtmbk.setRowCount(0);
        TampilAlamat(cari);
    }
