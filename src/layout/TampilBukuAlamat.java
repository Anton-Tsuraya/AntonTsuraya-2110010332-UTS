    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        DefaultTableModel  dtmbk = (DefaultTableModel) tblBuku.getModel();
        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(this, "Yakin untuk Hapus Data Ini", "Hapus", dialogButton);
        if (dialogResult == 0) {
            dtmbk.removeRow(row);
            bkalamat.remove(row);
            dtmbk.setRowCount(0);
            for (int i = 0; i < bkalamat.size(); i++) {
                Object[] objs = {bkalamat.get(i).id, bkalamat.get(i).nama, bkalamat.get(i).telepon,
                                 bkalamat.get(i).kelamin, bkalamat.get(i).alamat};
                dtmbk.addRow(objs);
                }
            }
    }//GEN-LAST:event_btnHapusActionPerformed

