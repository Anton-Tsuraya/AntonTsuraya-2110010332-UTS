    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        int i = tblBuku.getSelectedRow();
        if(i>=0) {
            TableModel dtmbk = tblBuku.getModel();
            buku = new Buku();
            buku.setId(Integer.parseInt(dtmbk.getValueAt(i, 0).toString()));
            buku.setNama(dtmbk.getValueAt(i, 1).toString());
            buku.setTelepon(dtmbk.getValueAt(i, 2).toString());
            buku.setKelamin(dtmbk.getValueAt(i, 3).toString());
            buku.setAlamat(dtmbk.getValueAt(i, 4).toString());
            
            
            EditBukuAlamat editbukualamat = new EditBukuAlamat(buku);
            editbukualamat.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Pilih Data yang Ingin Diubah");
        }
    }//GEN-LAST:event_btnEditActionPerformed

